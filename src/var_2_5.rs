pub fn var_2_5() {
    // 불변변수 예제
    let x = 5; // 불변변수 x를 선언하고 값 5를 할당합니다.
    println!("불변변수 x의 값: {}", x);

    // 가변변수 예제
    let mut y = 10; // 가변변수 y를 선언하고 값 10을 할당합니다.
    println!("가변변수 y의 초기 값: {}", y);
    
    y = 15; // 가변변수 y의 값을 변경합니다.
    println!("가변변수 y의 변경된 값: {}", y);

    // 불변변수의 값 변경 시도
    // x = 8; // 에러 발생! 불변변수는 값을 변경할 수 없습니다.
}
