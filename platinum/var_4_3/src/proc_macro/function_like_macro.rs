/*여기서 $x:expr는 매크로의 인자로 표현식을 받는 것을 의미합니다. 따라서 사용자는 매크로를 호출할 때 표현식을 제공해야 합니다.
let val = $x; 부분은 매크로 내에서 $x를 값으로 복사하여 새로운 변수 val에 할당하는 것입니다. 
이렇게 함으로써 매크로 내에서 $x에 여러 번 접근할 때, $x의 값을 변경하지 않고 여러 번 사용할 수 있습니다.
val * val 부분은 val의 제곱을 계산하는 것입니다.
*/
#[macro_export]
macro_rules! square {
    ($x:expr) => {{
        let val = $x;
        val * val
    }};
}