pub fn var_2_17(){
    
    //소유권 개념 
    /*  1. 각 값은 러스트에서 소유자(owner)를 갖습니다.
        2. 한 번에 하나의 소유자만 있을 수 있습니다.
        3. 소유자가 범위를 벗어나면 값은 삭제(drop)됩니다.
    */
    let s1 = String::from("Hello");
    let s2 = s1; // s1이 s2로 소유권을 이전
    // 아래 줄의 주석을 해제하면 컴파일 에러 발생
    // println!("s1: {}", s1);
    println!("s2: {}", s2);
}
    